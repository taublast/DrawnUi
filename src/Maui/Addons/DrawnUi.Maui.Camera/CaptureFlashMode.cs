namespace DrawnUi.Camera
{
    /// <summary>
    /// Flash mode for still image capture
    /// </summary>
    public enum CaptureFlashMode
    {
        /// <summary>
        /// Flash is disabled for capture
        /// </summary>
        Off,
        /// <summary>
        /// Flash fires automatically based on lighting conditions
        /// </summary>
        Auto,
        /// <summary>
        /// Flash always fires during capture
        /// </summary>
        On
    }
}
